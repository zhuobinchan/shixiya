package com.dayee.wintalent.service.v8.pojo;

import javax.persistence.Entity;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlElement;

@Table(name="dayyBasicInfo")
@Entity
public class BasicInfos {

	private String PostId; //职位ID
	private String projectId;//招聘项目ID
	private String PostName;  //职位名称
	private String ExternalPostName;//职位对外名称
	private String PostOrgName;//机构名称
	private String AddDate;//添加日期
	private String ApplyDate;//应聘日期
	private String RecruitType;//(招聘类型 1校园，2社会，3内招，4猎头，8内部推荐，12实习生，13海外)
	private String ResumeSource;//简历来源
	private String NetChannelId;//渠道ID
	private String ChannelDicId;//渠道字典ID
	private String ChannelName;//渠道名称
	private String ChannelOrgId;//渠道机构
	private String ChannelOrgName;//渠道机构名称
	private String SubmitPreHireAuditDate;//提交拟录用审核日期
	private String ExpectedRecruitment;//拟入职日期
	
	
	
	
	public BasicInfos() {
		super();
	}

	public BasicInfos(String postId, String projectId, String postName,
			String externalPostName, String postOrgName, String addDate,
			String applyDate, String recruitType, String resumeSource,
			String netChannelId, String channelDicId, String channelName,
			String channelOrgId, String channelOrgName,
			String submitPreHireAuditDate, String expectedRecruitment) {
		super();
		PostId = postId;
		this.projectId = projectId;
		PostName = postName;
		ExternalPostName = externalPostName;
		PostOrgName = postOrgName;
		AddDate = addDate;
		ApplyDate = applyDate;
		RecruitType = recruitType;
		ResumeSource = resumeSource;
		NetChannelId = netChannelId;
		ChannelDicId = channelDicId;
		ChannelName = channelName;
		ChannelOrgId = channelOrgId;
		ChannelOrgName = channelOrgName;
		SubmitPreHireAuditDate = submitPreHireAuditDate;
		ExpectedRecruitment = expectedRecruitment;
	}

	@XmlElement(name="PostId")
	public String getPostId() {
		return PostId;
	}

	
	public void setPostId(String postId) {
		PostId = postId;
	}
	@XmlElement(name="ProjectId")
	public String getProjectId() {
		return projectId;
	}

	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	@XmlElement(name="PostName")
	public String getPostName() {
		return PostName;
	}

	public void setPostName(String postName) {
		PostName = postName;
	}
	@XmlElement(name="ExternalPostName")
	public String getExternalPostName() {
		return ExternalPostName;
	}

	public void setExternalPostName(String externalPostName) {
		ExternalPostName = externalPostName;
	}
	@XmlElement(name="PostOrgName")
	public String getPostOrgName() {
		return PostOrgName;
	}

	public void setPostOrgName(String postOrgName) {
		PostOrgName = postOrgName;
	}
	@XmlElement(name="AddDate")
	public String getAddDate() {
		return AddDate;
	}

	public void setAddDate(String addDate) {
		AddDate = addDate;
	}
	@XmlElement(name="ApplyDate")
	public String getApplyDate() {
		return ApplyDate;
	}

	public void setApplyDate(String applyDate) {
		ApplyDate = applyDate;
	}
	@XmlElement(name="RecruitType")
	public String getRecruitType() {
		return RecruitType;
	}

	public void setRecruitType(String recruitType) {
		RecruitType = recruitType;
	}
	@XmlElement(name="ResumeSource")
	public String getResumeSource() {
		return ResumeSource;
	}

	public void setResumeSource(String resumeSource) {
		ResumeSource = resumeSource;
	}
	@XmlElement(name="NetChannelId")
	public String getNetChannelId() {
		return NetChannelId;
	}

	public void setNetChannelId(String netChannelId) {
		NetChannelId = netChannelId;
	}
	@XmlElement(name="ChannelDicId")
	public String getChannelDicId() {
		return ChannelDicId;
	}

	public void setChannelDicId(String channelDicId) {
		ChannelDicId = channelDicId;
	}
	@XmlElement(name="ChannelName")
	public String getChannelName() {
		return ChannelName;
	}

	public void setChannelName(String channelName) {
		ChannelName = channelName;
	}
	@XmlElement(name="ChannelOrgId")
	public String getChannelOrgId() {
		return ChannelOrgId;
	}

	public void setChannelOrgId(String channelOrgId) {
		ChannelOrgId = channelOrgId;
	}
	@XmlElement(name="ChannelOrgName")
	public String getChannelOrgName() {
		return ChannelOrgName;
	}

	public void setChannelOrgName(String channelOrgName) {
		ChannelOrgName = channelOrgName;
	}
	@XmlElement(name="SubmitPreHireAuditDate")
	public String getSubmitPreHireAuditDate() {
		return SubmitPreHireAuditDate;
	}

	public void setSubmitPreHireAuditDate(String submitPreHireAuditDate) {
		SubmitPreHireAuditDate = submitPreHireAuditDate;
	}
	@XmlElement(name="ExpectedRecruitment")
	public String getExpectedRecruitment() {
		return ExpectedRecruitment;
	}

	public void setExpectedRecruitment(String expectedRecruitment) {
		ExpectedRecruitment = expectedRecruitment;
	}

	@Override
	public String toString() {
		return "BasicInfos [PostId=" + PostId + ", projectId=" + projectId
				+ ", PostName=" + PostName + ", ExternalPostName="
				+ ExternalPostName + ", PostOrgName=" + PostOrgName
				+ ", AddDate=" + AddDate + ", ApplyDate=" + ApplyDate
				+ ", RecruitType=" + RecruitType + ", ResumeSource="
				+ ResumeSource + ", NetChannelId=" + NetChannelId
				+ ", ChannelDicId=" + ChannelDicId + ", ChannelName="
				+ ChannelName + ", ChannelOrgId=" + ChannelOrgId
				+ ", ChannelOrgName=" + ChannelOrgName
				+ ", SubmitPreHireAuditDate=" + SubmitPreHireAuditDate
				+ ", ExpectedRecruitment=" + ExpectedRecruitment + "]";
	}
	
	
	
}
