package com.dayee.wintalent.service.v8.pojo;

import javax.xml.bind.annotation.XmlElement;

public class WorkingExperiences {

	private String StartDate;//开始时间
	private String EndDate;//结束时间
	private String CompanyName;//企业名称
	private String TypeOfCompany;//企业性质
	private String WorkType;//工作性质
	private String CompanySize;//企业规模
	private String JobTitle;//职位名称
	private String Industry;//行业类别
	private String YearlySalaryBeforeTax;//职位年薪
	private String Department;//所在部门
	private String JobDescription;//工作描述
	private String ReasonForQuit;//离职原因
	
	public WorkingExperiences(String startDate, String endDate,
			String companyName, String typeOfCompany, String workType,
			String companySize, String jobTitle, String industry,
			String yearlySalaryBeforeTax, String department,
			String jobDescription, String reasonForQuit) {
		super();
		StartDate = startDate;
		EndDate = endDate;
		CompanyName = companyName;
		TypeOfCompany = typeOfCompany;
		WorkType = workType;
		CompanySize = companySize;
		JobTitle = jobTitle;
		Industry = industry;
		YearlySalaryBeforeTax = yearlySalaryBeforeTax;
		Department = department;
		JobDescription = jobDescription;
		ReasonForQuit = reasonForQuit;
	}
	
	

	public WorkingExperiences() {
		super();
	}


	@XmlElement(name="StartDate")
	public String getStartDate() {
		return StartDate;
	}

	public void setStartDate(String startDate) {
		StartDate = startDate;
	}
	@XmlElement(name="StartDate")
	public String getEndDate() {
		return EndDate;
	}

	public void setEndDate(String endDate) {
		EndDate = endDate;
	}
	@XmlElement(name="StartDate")
	public String getCompanyName() {
		return CompanyName;
	}

	public void setCompanyName(String companyName) {
		CompanyName = companyName;
	}
	@XmlElement(name="StartDate")
	public String getTypeOfCompany() {
		return TypeOfCompany;
	}

	public void setTypeOfCompany(String typeOfCompany) {
		TypeOfCompany = typeOfCompany;
	}
	@XmlElement(name="WorkType")
	public String getWorkType() {
		return WorkType;
	}

	public void setWorkType(String workType) {
		WorkType = workType;
	}
	@XmlElement(name="CompanySize")
	public String getCompanySize() {
		return CompanySize;
	}

	public void setCompanySize(String companySize) {
		CompanySize = companySize;
	}
	@XmlElement(name="JobTitle")
	public String getJobTitle() {
		return JobTitle;
	}

	public void setJobTitle(String jobTitle) {
		JobTitle = jobTitle;
	}
	@XmlElement(name="Industry")
	public String getIndustry() {
		return Industry;
	}

	public void setIndustry(String industry) {
		Industry = industry;
	}
	@XmlElement(name="YearlySalaryBeforeTax")
	public String getYearlySalaryBeforeTax() {
		return YearlySalaryBeforeTax;
	}

	public void setYearlySalaryBeforeTax(String yearlySalaryBeforeTax) {
		YearlySalaryBeforeTax = yearlySalaryBeforeTax;
	}
	@XmlElement(name="Department")
	public String getDepartment() {
		return Department;
	}

	public void setDepartment(String department) {
		Department = department;
	}
	@XmlElement(name="JobDescription")
	public String getJobDescription() {
		return JobDescription;
	}

	public void setJobDescription(String jobDescription) {
		JobDescription = jobDescription;
	}
	@XmlElement(name="ReasonForQuit")
	public String getReasonForQuit() {
		return ReasonForQuit;
	}

	public void setReasonForQuit(String reasonForQuit) {
		ReasonForQuit = reasonForQuit;
	}

	@Override
	public String toString() {
		return "[开始时间:" + StartDate + ", 结束时间:"
				+ EndDate + ", 企业名称:" + CompanyName + ", 企业性质:"
				+ TypeOfCompany + ", 工作性质:" + WorkType + ", 企业规模:"
				+ CompanySize + ", 职位名称:" + JobTitle + ", 行业类别:"
				+ Industry + ", 职位年薪:" + YearlySalaryBeforeTax
				+ ", 所在部门:" + Department + ", 工作描述:"
				+ JobDescription + ", 离职原因:" + ReasonForQuit + "]";
	}
	

	
}
