package com.ehighsun.shixiya.websocket;

import java.util.Map;

public class HrCheckAnswer {
	private Integer studentId;
	private String headImg;
	private String name;
	private String questionTitle;
	private Map<Map<Integer, String>, String> answerMap;// key值为问题 value值为答案

	public String getHeadImg() {
		return headImg;
	}

	public void setHeadImg(String headImg) {
		this.headImg = headImg;
	}

	public Integer getStudentId() {
		return studentId;
	}

	public void setStudentId(Integer studentId) {
		this.studentId = studentId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getQuestionTitle() {
		return questionTitle;
	}

	public void setQuestionTitle(String questionTitle) {
		this.questionTitle = questionTitle;
	}

	public Map<Map<Integer, String>, String> getAnswerMap() {
		return answerMap;
	}

	public void setAnswerMap(Map<Map<Integer, String>, String> answerMap) {
		this.answerMap = answerMap;
	}

}
